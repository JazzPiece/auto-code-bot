def double_numbers(numbers):
    return list(map(lambda x: x * 2, numbers))

def triple_numbers(numbers):
    return list(map(lambda x: x * 3, numbers))

def square_numbers(numbers):
    return list(map(lambda x: x ** 2, numbers))

def cube_numbers(numbers):
    return list(map(lambda x: x ** 3, numbers))