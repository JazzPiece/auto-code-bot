def sum_array(arr):
    return sum(arr)

def average_array(arr):
    return sum(arr) / len(arr)

def max_value(arr):
    return max(arr)

def min_value(arr):
    return min(arr)

def reverse_array(arr):
    return arr[::-1]

def sort_array(arr):
    return sorted(arr)

def merge_arrays(arr1, arr2):
    return arr1 + arr2

def remove_duplicates(arr):
    return list(set(arr))